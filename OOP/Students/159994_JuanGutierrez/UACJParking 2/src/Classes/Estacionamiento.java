public class Estacionamiento {
    
    public int sumaCajones(int cantCajonesDisc, int cantCajonesNorm)
    {
        return cantCajonesDisc + cantCajonesNorm;
    }
    
    public int sumaCajonesUtil(int cantCajonesDiscUtil, int cantCajonesNormUtil)
    {
        return cantCajonesDiscUtil + cantCajonesNormUtil;
    }
    
    
}
